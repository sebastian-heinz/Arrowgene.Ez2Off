    public class NoteHistory
    {
        public static NoteHistory MaxScoreHistory(int totalNotes)
        {
            NoteHistory history = new NoteHistory();
            for (int i = 0; i < totalNotes; i++)
            {
                history.Add(NoteType.Kool);
            }

            history.Calculate();
            return history;
        }
        
        private readonly List<NoteType> _history;

        public int Score { get; private set; }
        public int MaxCombo { get; private set; }

        public NoteHistory()
        {
            _history = new List<NoteType>();
            Score = 0;
            MaxCombo = 0;
        }

        public void Clear()
        {
            _history.Clear();
            Score = 0;
            MaxCombo = 0;
        }

        public void AddRange(IEnumerable<NoteType> notes)
        {
            _history.AddRange(notes);
        }

        public void Add(NoteType note)
        {
            _history.Add(note);
        }

        public int NoteCount()
        {
            return _history.Count;
        }

        public void Calculate()
        {
            double totalScore = 0;
            int combo = 0;
            int maxCombo = 0;
            foreach (NoteType note in _history)
            {
                double awardScore = 0;
                switch (note)
                {
                    case NoteType.Fail:
                        combo = 0;
                        break;
                    case NoteType.Miss:
                        combo = 0;
                        break;
                    case NoteType.Good:
                        combo++;
                        awardScore = Calculate(40, combo);
                        break;
                    case NoteType.Cool:
                        combo++;
                        awardScore = Calculate(100, combo);
                        break;
                    case NoteType.Kool:
                        combo++;
                        awardScore = Calculate(170, combo);
                        break;
                }

                if (combo == 850)
                {
                    awardScore++;
                }

                if (combo > maxCombo)
                {
                    maxCombo = combo;
                }

                totalScore += awardScore;
            }

            Score = (int) totalScore;
            MaxCombo = maxCombo;
        }

        private double Calculate(int score, int combo)
        {
            if (combo > 1499)
            {
                combo = 1499;
            }

            float multiplier = lookupTable[combo];
            return Math.Truncate(score * multiplier);
        }

